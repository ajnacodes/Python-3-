# while loops
# while something, do something else
# while something_is_true
#   do something repeatedly


# print a sentence 10 times
# define counter as 0
counter = 0

# define the while loop
while counter < 10:
    # increase the counter with 1, so that it doesn't go infinite
    counter += 1
    print(f"This is the loop number : {counter}")
    
    
